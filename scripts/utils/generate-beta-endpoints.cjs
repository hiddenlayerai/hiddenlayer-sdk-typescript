// @ts-check
const fs = require('fs');
const path = require('path');

const resourcesDir = path.resolve(__dirname, '..', '..', 'src', 'resources');
const outputFile = path.resolve(__dirname, '..', '..', 'src', 'lib', 'beta-endpoints.ts');

/**
 * Recursively yields all file paths under `dir`.
 * @param {string} dir
 * @returns {AsyncGenerator<string>}
 */
async function* walk(dir) {
  for await (const d of await fs.promises.opendir(dir)) {
    const entry = path.join(dir, d.name);
    if (d.isDirectory()) yield* walk(entry);
    else if (d.isFile()) yield entry;
  }
}

/**
 * Extracts the class name from an `export class Foo extends APIResource` line.
 * @param {string} source
 * @returns {string | null}
 */
function extractClassName(source) {
  const match = source.match(/export\s+class\s+(\w+)\s+extends\s+APIResource/);
  return match ? match[1] : null;
}

/**
 * Finds beta methods in a resource file and returns their URL-path-to-method mappings.
 *
 * Strategy: walk through the source looking for JSDoc blocks containing [BETA].
 * When found, the immediately following method declaration gives us the method name.
 * The method body's `this._client.<verb>('/path', ...)` call gives us the URL path.
 *
 * @param {string} source
 * @param {string} className
 * @returns {Array<{ path: string, method: string }>}
 */
function extractBetaMethods(source, className) {
  const results = [];

  // Match JSDoc blocks that contain [BETA], followed by a method declaration.
  // The regex captures the method name and the body up to the next JSDoc or end of class.
  const betaPattern = /\/\*\*[\s\S]*?\[BETA\][\s\S]*?\*\/\s*(\w+)\s*\(/g;

  let match;
  while ((match = betaPattern.exec(source)) !== null) {
    const methodName = match[1];

    // Find the method body starting from this match position.
    // Look for the this._client.<verb>('/path', ...) call.
    const afterMethod = source.slice(match.index);
    const urlMatch = afterMethod.match(/this\._client\.(?:post|get|put|patch|delete)\(\s*'([^']+)'/);

    if (urlMatch) {
      results.push({
        path: urlMatch[1],
        method: `${className}.${methodName}`,
      });
    }
  }

  return results;
}

async function main() {
  /** @type {Array<{ path: string, method: string }>} */
  const entries = [];

  for await (const file of walk(resourcesDir)) {
    if (!file.endsWith('.ts')) continue;

    const source = await fs.promises.readFile(file, 'utf8');
    const className = extractClassName(source);
    if (!className) continue;

    const methods = extractBetaMethods(source, className);
    entries.push(...methods);
  }

  // Sort by path for stable output
  entries.sort((a, b) => a.path.localeCompare(b.path));

  const lines = entries.map((e) => `  '${e.path}': '${e.method}',`);

  const output = `/**
 * Auto-generated registry of beta endpoints.
 * DO NOT EDIT — regenerated by scripts/utils/generate-beta-endpoints.cjs
 *
 * Maps URL paths to qualified method names for runtime warnings.
 */
export const BETA_ENDPOINTS: Record<string, string> = {
${lines.join('\n')}
};
`;

  await fs.promises.writeFile(outputFile, output, 'utf8');

  console.log(`Wrote ${entries.length} beta endpoint(s) to ${path.relative(process.cwd(), outputFile)}`);
  for (const e of entries) {
    console.log(`  ${e.path} -> ${e.method}`);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
